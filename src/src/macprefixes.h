  mac_address.replace(":", "");
  MacEntry* mac_arrays[] = {mac_entries_0, mac_entries_1, mac_entries_2,
                            mac_entries_3, mac_entries_4, mac_entries_5,
                            mac_entries_6, mac_entries_7, mac_entries_8};
